/*Q 2. What is OOP? List OOP concepts 
Ans - Object-Oriented Programming (OOP) is a programming paradigm that uses "objects" 
to design and develop software. These objects are instances of classes, which are 
blueprints defining the properties (attributes) and behaviors (methods) that the objects will have.

Key OOP Concepts:

Class: A blueprint for creating objects, defining their properties and behaviors.

Object: An instance of a class, representing a real-world entity.

Encapsulation: Bundling data (attributes) and methods (functions) that operate on the data 
into a single unit (class), and restricting access to some of the object's components.

Inheritance: A mechanism where a new class (child class) inherits attributes and methods 
from an existing class (parent class).

Polymorphism: The ability to present the same interface for different underlying forms
 (methods with the same name can behave differently based on the object calling them).
Abstraction: Simplifying complex reality by modeling classes appropriate to the problem, 
and working at the most relevant level of inheritance for the task.
These concepts help in organizing code, making it more modular, reusable, and easier to maintain

Q3. What is the difference between OOP and POP?
Ans-The primary difference between Object-Oriented Programming (OOP) and Procedural-Oriented 
Programming (POP) lies in how they structure and manage code.

OOP (Object-Oriented Programming):

Focus: Centers around objects and classes.

Structure: Uses objects to represent real-world entities, with data and methods encapsulated within these objects.

Key Concepts: Encapsulation, Inheritance, Polymorphism, and Abstraction.
Example Languages: C++, Java, Python.

POP (Procedural-Oriented Programming):

Focus: Centers around functions and procedures.

Structure: Follows a linear, top-down approach where the program is divided into functions or procedures 
that perform specific tasks.

Key Concepts: Function calls, modularity.

Example Languages: C, Pascal, Fortran.
In summary, OOP emphasizes objects and their interactions, while POP emphasizes a sequence of procedures or 
steps to perform tasks. */


